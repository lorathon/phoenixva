								<section class="toggle active">
									<label>What is Phoenix Virtual Airways?</label>
									<p>Phoenix VA is comprised of a group of flight simulation enthusiasts who seek purpose and structure to flying on their PC. We offer a wealth of resources and options for you to customize your experience and suit your taste. Our pilots search for and bid on flights from our system and fly the flight on their flight simulation software. The flight is logged using our propietary, state of the art ACARS system and sends the flight data back to our system where the flight is logged and recorded. Ranks are assigned and awarded by flight hours and higher rank allows you to fly larger equipment types.</p>
								</section>

								<section class="toggle">
									<label>What flight simulators are supported by PVA?</label>
									<p>PVA currently supports Microsoft's Flight Simulator X, FS2004 and also supports Prepar3d.</p>
								</section>

								<section class="toggle">
									<label>Do I need to be connected to the internet to use PVA?</label>
									<p>PVA is an online community and internet connection is needed to bid on and load flights into your flight simulation software. A constant connection isn't required throughout the flight, but is highly recommended. </p>
								</section>

								<section class="toggle">
									<label>How often do I have to fly to remain as an active member?</label>
									<p>PVA requires you to fly and submit a flight within 7 days of joining or being re-activated. After that you must flight and submit at least one flight within 30 days. If you do not meet the requirement you will be automatically retired from PVA. Your account including hours, ranks and awards will remain in the system and available to you if you wish to be re-activated.</p>
								</section>

								<section class="toggle">
									<label>What if I know I won't be able to meet the flight requirements for a while?</label>
									<p>You can request a Leave of Absense (LOA) from PVA for as long as you wish. When you are ready to return, you will be re-activated, keeping all your flight hours, rankings and awards.</p>
								</section>

								<section class="toggle">
									<label>Can I transfer hours from a previous or current virtual airline I am a member of?</label>
									<p>PVA allows for a one-time transfer of up to 150 flight hours from another VA. These must be verified by our staff before the hours are awarded.</p>
								</section>

								<section class="toggle">
									<label>Would I be required to use VATSIM Air Traffic Control for all my flights?</label>
									<p>No. We offer training and bonus hours in some events for using VATSIM during your flights and it is recommended for the best experience, but not required.</p>
								</section>

								<section class="toggle">
									<label>How accurate are your flight schedules compared to real-world airlines?</label>
									<p>PVA obtains actual, real-world flight schedules from the leading flight data providers. The data is updated regularly and parsed into our system to offer you flight options and realism that is not found anywhere else. We obtain all real-world scheduled service from over 1,200 airlines covering over 4,700 destinations and allow you to fly them using the same flight numbers and times as in real-world operations.</p>
								</section>

								<section class="toggle">
									<label>Is it just passenger jet service that's available?</label>
									<p>No. PVA allows you to fly all real-world scheduled passenger and cargo operations. Whether it be in a seaplane, helicopter, Cessna light aircraft or Airbus A380. We offer it all and everything in between.</p>
								</section>
								
								<section class="toggle">
									<label>Can I fly from/to wherever I want?</label>
									<p>PVA strives to achieve the most realistic flight operations as possible. The destinations are endless, but we require you to depart from the airport that you last landed at. You can opt to waive this rule and fly from a different airport, but you might be charged a "jumpseat fee" which consists of docking flight hours from your logbook. We do however allow jumpseat waivers for some special circumstances and scheduled events, which are approved by our staff. Premium members are exempt from this rule.</p>
								</section>
								
								<section class="toggle">
									<label>Can I fly long haul flights while I sleep to rack up hours?</label>
									<p>In keeping with our goal of realism and fairness, we have guidelines prohibiting flying "Away from the Keyboard" for extended periods of time. This is also enforced through our ACARS system with random checks throughout the flight asking you to confirm you are near the cockpit. We have had great response to this policy because it corrected a frequent complaint that some pilots would set up a flight, leave it on autopilot for 12+ hours while they went to school, work, bed, etc and came back in and landed it, getting unfair credit of hours and skewing contests and rankings.</p>
								</section>
								
								<section class="toggle">
									<label>What is premium membership?</label>
									<p>Premium members are able to fly any aircraft regardless of their rank or hours, commonly known at PVA as "CAT-Free". They can depart from any airport regardless of where they last landed and not receive any penalties, also known as "Jumpseat Free". They have access to our full database of over 15,000 airports throughout the world and have the ability to create their own VFR or IFR flight plans between any of them, while getting credit for the flight hours. This includes anything from a VFR flight in a cessna light aircraft to an IFR charter flight in a business jet or jumbo jet. They have access to certain staff forums to help shape the future of PVA.</p>
								</section>
								
								<section class="toggle">
									<label>How do I become a premium member?</label>
									<p>Premium membership can be obtained by paying a small monthly membership fee. We offer premium services to help offset the cost of building and maintaining our system and obtaining flight schedule data. Premium membership is also available to "PVA Award" recipients, pilots with extensive and verifiable real-world flight hours, and at the discretion of the founders.</p>
